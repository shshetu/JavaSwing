public boolean checkEmail(String email){
String epattern = "^[A-z0-9._%+-]+@[A-z0-9.-]+\\.[A-Z][2,6]$";
Pattern p = Pattern.compile(epattern,Pattern.CASE_INSENSITIVE);
Matcher m = p.matcher(email);
return m.matches();
}
    private void jButtonAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAddActionPerformed
//validation
        if (jTextFieldId.getText().trim().length() < 5) {
            JOptionPane.showMessageDialog(null, "Id should be atleast 5 characters!");
        } else if (jTextFieldName.getText().trim().length() < 4) {
            JOptionPane.showMessageDialog(null, "Name should be atleast 4 characters!");
        } else if (jTextFieldAge.getText().trim().length() < 1) {
            JOptionPane.showMessageDialog(null, "Please Enter your age!");
        } else if (Integer.parseInt(jTextFieldAge.getText().trim())<18 || Integer.parseInt(jTextFieldAge.getText().trim())>100) {
            JOptionPane.showMessageDialog(null, "Age Range is between 18 and 100!");
        }else if (checkEmail(jTextFieldEmail.getText().trim()) && jTextFieldEmail.getText().trim().length()<1) {
            JOptionPane.showMessageDialog(null, "Enter valid email!");
        }else if (!jRadioButtonMale.isSelected() || !jRadioButtonFemale.isSelected()){
            JOptionPane.showMessageDialog(null, "Enter your gender!");
        }else if(!jCheckBoxHTML.isSelected() && !jCheckBoxJava.isSelected() && !jCheckBoxSpring.isSelected()) {
            JOptionPane.showMessageDialog(null, "Enter the completed courses!");
        }else if (jComboBoxRound.getItemAt(jComboBoxRound.getSelectedIndex()) == "Select A ROund") {
            JOptionPane.showMessageDialog(null, "Select your round!");
        }else if (jTextAreaComments.getText().trim().length()<10) {
            JOptionPane.showMessageDialog(null, "Comments should be atleast 10 characters!");
        }
        else {
            int id = Integer.parseInt(jTextFieldId.getText().trim());
            String name = jTextFieldName.getText().trim();
            int age = Integer.parseInt(jTextFieldAge.getText().trim());;
            String email = jTextFieldEmail.getText().trim();;
            String gender = "";
            if (jRadioButtonMale.isSelected()) {
                gender = jRadioButtonMale.getText();
            }
            if (jRadioButtonFemale.isSelected()) {
                gender = jRadioButtonFemale.getText();
            }
            String course = "";
            if (jCheckBoxHTML.isSelected()) {
                course += jCheckBoxHTML.getText();
            }
            if (jCheckBoxJava.isSelected()) {
                course += " " + jCheckBoxJava.getText();
            }
            if (jCheckBoxSpring.isSelected()) {
                course += " " + jCheckBoxSpring.getText();
            }
            String round = jComboBoxRound.getItemAt(jComboBoxRound.getSelectedIndex());
            String comment = jTextAreaComments.getText().trim();
            String result = id + " " + name + " " + age + " " + email + " " + gender + " " + course + " " + round + " " + comment;

            //Add to the table
            DefaultTableModel model = (DefaultTableModel) jTableDisplay.getModel();
            Object cols[] = new Object[8];
            cols[0] = id;
            cols[1] = name;
            cols[2] = age;
            cols[3] = email;
            cols[4] = gender;
            cols[5] = course;
            cols[6] = round;
            cols[7] = comment;
            model.addRow(cols);
            JOptionPane.showMessageDialog(null, "data is successfully inserted into the table!");
            //write to the table
            Student s = new Student(id, name, age, email, gender, course, round, comment);
            List<Student> list = new ArrayList<>();
            list.add(s);
            //call the writeinto file method
            Utils.WriteToFile("shetu", list);
            JOptionPane.showMessageDialog(null, "Data successfully inserted into the file!");
        }
    }//GEN-LAST:event_jButtonAddActionPerformed

