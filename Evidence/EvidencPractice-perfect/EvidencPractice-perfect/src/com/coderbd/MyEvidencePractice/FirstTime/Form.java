    private void jButtonAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAddActionPerformed
        // TODO add your handling code here:
        //Validation
        
        //Add data
        int id = Integer.parseInt(jTextFieldId.getText());
        String name = jTextFieldName.getText();
        int age = Integer.parseInt(jTextFieldAge.getText());
        String email = jTextFieldEmail.getText();
        //gender
        String gender =" ";
        if(jRadioButtonMale.isSelected()){gender = jRadioButtonMale.getText();}
        if(jRadioButtonFemale.isSelected()){gender = jRadioButtonFemale.getText();}
        
        //C.Course
        String course ="";
        if(jCheckBoxHTML.isSelected()){course+=jCheckBoxHTML.getText();}
        if(jCheckBoxJava.isSelected()){course+=" "+jCheckBoxJava.getText();}
        if(jCheckBoxSwing.isSelected()){course+=" "+jCheckBoxSwing.getText();}
      
        //Round = ComboBox
        String round = jComboBoxRound.getItemAt(jComboBoxRound.getSelectedIndex());
        
        //comments
        String comments = jTextAreaComments.getText();
        
        //Total result:
        String result = id+"\n"+name+"\n"+age+"\n"+email+"\n"+gender+"\n"+course+"\n"+round+"\n"+comments;
        System.out.println(result);
       
        //Adding into Table
        DefaultTableModel model = (DefaultTableModel) jTableDisplay.getModel();
        Object row[] = new Object[8];
        row[0] = id;
        row[1] = name;
        row[2] = age;
        row[3] = email;
        row[4] = gender;
        row[5] = course;
        row[6] = round;
        row[7] = comments;
        model.addRow(row);
        
        ///writing into file
        
        Student s = new Student(id,name,age,email,gender,course,round,comments);
        //create a list of student type
        List<Student> list = new ArrayList<>();
        //add into the list
        list.add(s);
        try {
            Utils.writeToFile("shetu", list);
        } catch (Exception e) {
            
        }
        jLabelMessage.setText("Successfully Added into Table and written to File!");
    }//GEN-LAST:event_jButtonAddActionPerformed

